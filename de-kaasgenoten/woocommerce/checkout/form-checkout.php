<?php
/**
 * Checkout form.
 *
 * @package De_Kaasgenoten
 */

defined( 'ABSPATH' ) || exit;

do_action( 'woocommerce_before_checkout_form', $checkout );

if ( ! $checkout->is_registration_enabled() && $checkout->is_registration_required() && ! is_user_logged_in() ) {
	echo esc_html( apply_filters( 'woocommerce_checkout_must_be_logged_in_message', __( 'U moet ingelogd zijn om af te rekenen.', 'de-kaasgenoten' ) ) );
	return;
}
?>

<form name="checkout" method="post" class="checkout woocommerce-checkout dkg-checkout-form" action="<?php echo esc_url( wc_get_checkout_url() ); ?>" enctype="multipart/form-data" aria-label="<?php echo esc_attr__( 'Afrekenen', 'de-kaasgenoten' ); ?>">

	<?php if ( $checkout->get_checkout_fields() ) : ?>

		<?php do_action( 'woocommerce_checkout_before_customer_details' ); ?>

		<div class="dkg-checkout-grid" id="customer_details">
			<div class="dkg-checkout-col dkg-checkout-col--billing">
				<?php do_action( 'woocommerce_checkout_billing' ); ?>
			</div>
			<div class="dkg-checkout-col dkg-checkout-col--shipping">
				<?php do_action( 'woocommerce_checkout_shipping' ); ?>
			</div>
		</div>

		<?php do_action( 'woocommerce_checkout_after_customer_details' ); ?>

	<?php endif; ?>

	<?php do_action( 'woocommerce_checkout_before_order_review_heading' ); ?>

	<h3 id="order_review_heading"><?php esc_html_e( 'Uw bestelling', 'de-kaasgenoten' ); ?></h3>

	<?php do_action( 'woocommerce_checkout_before_order_review' ); ?>

	<div id="order_review" class="woocommerce-checkout-review-order dkg-checkout-review">
		<?php do_action( 'woocommerce_checkout_order_review' ); ?>
	</div>

	<?php do_action( 'woocommerce_checkout_after_order_review' ); ?>

</form>

<?php do_action( 'woocommerce_after_checkout_form', $checkout ); ?>
